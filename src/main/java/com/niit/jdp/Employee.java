package com.niit.jdp;

public class Employee {
    String name;
    long code;
    int age;
    String address;
    double salary;

    public Employee(String name, long code, int age, String address, double salary) {
        this.name = name;
        this.code = code;
        this.age = age;
        this.address = address;
        this.salary = salary;
    }
}
